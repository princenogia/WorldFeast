// Check if a recipe is available in our database
export function isRecipeAvailable(recipeName: string): boolean {
  return recipes.some(
    (recipe) => recipe.name.toLowerCase() === recipeName.toLowerCase()
  );
}

// Get matching recipe ID if available
export function getRecipeIdByName(recipeName: string): string | null {
  const recipe = recipes.find(
    (r) => r.name.toLowerCase() === recipeName.toLowerCase()
  );
  return recipe?.id || null;
}

// Get recipe suggestions for autocomplete
export function getRecipeSuggestions(query: string): Array<{
  name: string;
  available: boolean;
  id: string | null;
}> {
  if (!query || query.length < 2) return [];

  const lowerQuery = query.toLowerCase();
  const uniqueRecipes = [...new Set(allWorldRecipes)];

  return uniqueRecipes
    .filter((name) => name.toLowerCase().includes(lowerQuery))
    .slice(0, 8)
    .map((name) => ({
      name,
      available: isRecipeAvailable(name),
      id: getRecipeIdByName(name),
    }));
}
