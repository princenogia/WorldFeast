// Helper functions
export function getRecipesByContinent(continent: string): Recipe[] {
  return recipes.filter((recipe) => recipe.continent === continent);
}

export function getRecipesByCountry(countryCode: string): Recipe[] {
  return recipes.filter((recipe) => recipe.countryCode === countryCode);
}

export function getRecipeById(id: string): Recipe | undefined {
  return recipes.find((recipe) => recipe.id === id);
}

export function getContinentBySlug(slug: string): Continent | undefined {
  return continents.find((continent) => continent.slug === slug);
}

export function getCountriesByContinent(continentSlug: string): Country[] {
  const continent = continents.find((c) => c.slug === continentSlug);
  return continent?.countries || [];
}

export function searchRecipes(query: string): Recipe[] {
  const lowerQuery = query.toLowerCase();
  return recipes.filter(
    (recipe) =>
      recipe.name.toLowerCase().includes(lowerQuery) ||
      recipe.country.toLowerCase().includes(lowerQuery) ||
      recipe.tags.some((tag) => tag.toLowerCase().includes(lowerQuery)) ||
      recipe.continent.toLowerCase().includes(lowerQuery)
  );
}

export function getAllCountries(): Country[] {
  return continents.flatMap((c) => c.countries);
}

